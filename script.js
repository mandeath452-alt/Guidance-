const titles = Object.keys(DATA);
let currentIndex = 0;

function openDetail(title){
  currentIndex = titles.indexOf(title);

  const popup = document.getElementById("popup");
  const popupTitle = document.getElementById("popupTitle");
  const popupContent = document.getElementById("popupContent");

  popupTitle.innerText = title;

  let html = "";

  DATA[title].forEach(item=>{
    html += `<div style="margin-bottom:18px">`;

    if(item.arabic){
      html += `<p style="font-size:18px;margin-bottom:6px">${item.arabic}</p>`;
      html += `<p style="margin-bottom:6px">${item.roman}</p>`;
    }

    html += `<p>${item.english}</p>`;
    html += `<p style="opacity:0.85">${item.local}</p>`;
    html += `</div>`;
  });

  popupContent.innerHTML = html;
  popup.classList.add("show");
}

function nextDetail(){
  if(currentIndex < titles.length-1){
    currentIndex++;
    openDetail(titles[currentIndex]);
  }
}

function closeDetail(){
  document.getElementById("popup").classList.remove("show");
}
